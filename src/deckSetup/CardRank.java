package deckSetup;

/* In a standard deck, there are thirteen possible ranks a card can have. A card
 * must have one of these ranks to be usable for any of the card games in the engine package. */
public enum CardRank {
	ONE, TWO, THREE, FOUR, FIVE, SIX, SEVEN, EIGHT, NINE, TEN, JACK, QUEEN, KING, ACE;
}
