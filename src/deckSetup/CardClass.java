package deckSetup;

/* In a standard deck, there are fourteen possible classes a card can have. A card
 * must have one of these classes to be usable for any of the card games in the engine package. */
public enum CardClass {
	ONE, TWO, THREE, FOUR, FIVE, SIX, SEVEN, EIGHT, NINE, TEN, JACK, QUEEN, KING, ACE;
}
